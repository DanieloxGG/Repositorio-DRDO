# 11: Realiza un programa que introduciendo el valor del lado de un cuadrado nos devuelva 
# por pantalla en el área y el perímetro

lado=float(input("Introduce el lado (cm) del cuadrado: "))
print("")

print(f"El perímetro del cuadrado es de {lado*4} centímetros.")
print(f"El área del cuadrado es de {lado**2} centímetros cuadrados.")